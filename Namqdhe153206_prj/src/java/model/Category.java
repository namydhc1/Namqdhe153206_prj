
package model;

public class Category {
    private int cid;
    private String cname;

    public int getId() {
        return cid;
    }

    public void setId(int cid) {
        this.cid = cid;
    }

    public String getName() {
        return cname;
    }

    public void setName(String cname) {
        this.cname = cname;
    }
}
